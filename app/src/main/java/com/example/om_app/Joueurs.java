package com.example.om_app;

public class Joueurs {
    private String image;
    private String name;
    private Integer number;
    private String age;
    private String height;
    private String position;
    private String nationality;
    private String transfer_value;
    private String end_contract;

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getNumber() {
        return number;
    }

    public void setNumber(Integer number) {
        this.number = number;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public String getHeight() {
        return height;
    }

    public void setHeight(String height) {
        this.height = height;
    }

    public String getPosition() {
        return position;
    }

    public void setPosition(String position) {
        this.position = position;
    }

    public String getNationality() {
        return nationality;
    }

    public void setNationality(String nationality) {
        this.nationality = nationality;
    }

    public String getTransfer_value() {
        return transfer_value;
    }

    public void setTransfer_value(String transfer_value) {
        this.transfer_value = transfer_value;
    }

    public String getEnd_contract() {
        return end_contract;
    }

    public void setEnd_contract(String end_contract) {
        this.end_contract = end_contract;
    }
}
